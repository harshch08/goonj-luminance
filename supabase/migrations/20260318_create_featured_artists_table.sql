-- Create featured_artists table (for the "Meet Our Artists" section on /artists page)
CREATE TABLE IF NOT EXISTS featured_artists (
  id INTEGER PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  category VARCHAR(100) NOT NULL,
  genre VARCHAR(255) NOT NULL,
  image TEXT NOT NULL,
  bio TEXT NOT NULL,
  performances VARCHAR(50) NOT NULL DEFAULT '100+',
  rating DECIMAL(2,1) NOT NULL DEFAULT 4.8,
  is_active BOOLEAN DEFAULT TRUE,
  sort_order INTEGER DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_featured_artists_active ON featured_artists(is_active);
CREATE INDEX IF NOT EXISTS idx_featured_artists_sort ON featured_artists(sort_order);

